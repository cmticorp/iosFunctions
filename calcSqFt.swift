import Foundation

class calculateThings: NSObject {
    
    func  calcSqFt(zheight: Float, zwidth: Float) -> Float {
        var zsqft: Float = 0
        
        zsqft = zheight * zwidth
        
        return zsqft 
    }
    
    
    
    
    
